EAMCET: “Engineering Agriculture and Medicine Common Entrance Test”<br><br>
EAMCET Entrance Examination is the gateway for entry into various professional courses offered in Government/Private Telangana engineering colleges. The EAMCET test is taken by a large number of engineering and medical aspirants in the state. Presently, JNTU located in Hyderabad conducts the examination on behalf of TSCHE. Qualifying EAMCET Exam is compulsory to get admission into B.Tech courses through counselling quota. In every branch, 70% of seats are filled through web counselling. The remaining 30% of seats in every branch are filled by management (B category or Management Quota). For details on web counselling and giving options in web kindly click the given below web link.<br>
https://tseamcet.nic.in<br><br>

CEEP: “Common Entrance Examination for Admission into Polytechnic”<br><br>
The State Board of Technical Education and Training, Telangana State, Hyderabad will conduct “Common Entrance Examination for Admission into Polytechnic” or the candidates seeking admission to all Diploma Courses in Engineering /Non-Engineering/ Technology offered at Polytechnics/ Institutions (including Aided and Unaided Private Polytechnics/ Institutions) in Telangana State.
Detailed Information regarding eligibility, age, syllabus, etc., are given in the “CEEP-2019 Booklet” which is supplied along with ICR FORM and is available for sale at a cost of Rs.10/- (Rupees Ten only) from the Principals of Govt. Polytechnic, Private Polytechnics and TS online centres. Details can be seen on website.<br>
https://tspolycet.nic.in<br><br>




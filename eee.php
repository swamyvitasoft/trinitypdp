      <h2 style="color: #003366;">About Department</h2><br>
      <p style="text-indent: 50px;" align:"justify">The Department of Electrical Engineering was formed with the primary
        objective of providing quality education in the field of Electrical Engineering,
        while addressing the problems of today and tomorrow. Right from its inception, the department has been offering
        excellent infrastructural facilities with a
        variety of platforms for aspiring professional students to meet the growing demands of the Electrical industry.
      </p>
      <p style="text-indent: 50px;" align:"justify">The Department of Electrical and Electronics Engineering was
        established in 2008 with Undergraduate Programme B.Tech.
        Electrical and Electronics Engineering. The department endeavours to produce confident professionals tuned to
        real time working environment.
        The department offers excellent academic environment with a team of highly qualified faculty members to inspire
        the students to develop their technical skills and inculcate the spirit of team work in them.
        The strength of the department lies in the highly motivated students who understand the dynamics of the industry
        and hone their skills accordingly.
        We focus on bridging the gap between campus education and industry needs through internships, guest lectures and
        workshops. </p>
      <br>
      <h4>Laboratory Facilities</h4><br>
      <ul>
        <li>Electrical Machines Laboratory</li>
        <li>Power Electronics Laboratory</li>
        <li>Control System Laboratory</li>
        <li>Power System Laboratory</li>
        <li>Measurement and Instrumentation Laboratory</li>
        <li>Electric Circuits Laboratory</li>
        <li>Engineering Workshop Laboratory</li>
      </ul>
      <br>
      <h4>Electrical Machines Laboratory</h4>
      <p align:"justify">The Electrical Machines Laboratory supports teaching; research and consultancy work on
        Electrical machines and drives.
        Through hands-on experiments with real machines, students gain practical experience on transformers and various
        types of machine drives.
        The laboratory can also be used for project work related to electrical machines and energy conversion.</p>
      <br>
      <h4>Power Electronics Laboratory</h4>
      <p align:"justify">Circuits and devices used for switching power converters, solid-state motor drives, and power
        controllers; dc-dc, ac-dc, and dc-ac converters and applications;
        high-power transistors and magnetic components; design considerations including heat transfer.</p><br>
      <h4>Control System Laboratory</h4>
      <p align:"justify">The Control Systems Laboratory introduces students to the implementation of PID-controllers,
        and two-step controllers, to first order delay, as well as third order delay,
        systems using educational PID boards and DC servo boards. Six numbers of PLC kits available for doing industrial
        application based experiments.
        Experimentations and analysis use industrial standard oscilloscopes, and data-acquisition boards interfaced via
        SIMULINK/MATLAB.</p><br>
      <h4>Power System Laboratory</h4>
      <p align:"justify">Power System Simulation Laboratory is a computer model that mimics the operation of a real or
        proposed Power Engineering system.
        Simulation tool helps in validating the ideas and new concepts which born’s in an engineer’s mind which seeks
        for solution to a problem.
        Sophisticated Computer programs are used in Power Engineering Simulation Laboratory for the purpose of analysis
        and design in the following domains.</p><br>
      <h4>Measurement and Instrumentation Laboratory</h4>
      <p align:"justify">The Measurement and Instrumentation laboratory in Electrical and Electronics Engineering (EEE)
        Department has the objective to familiarize the
        student with the operation of basic laboratory instrumentation such as Energy meter, multimeter, frequency
        counter, voltmeter, Ammeter and also with personal
        computers and circuit simulators such as Pspice and Workbench etc.
        To understand the experiments student must understand the theory of the circuits under test, and the instruments
        used to test them.</p><br>
      <h4>Electric Circuits Laboratory</h4>
      <p align:"justify">In the Electrical Circuit Lab students can create their own electrical circuits and do
        measurements on it.
        The electrical circuits are limited to DC (direct current). There is also a version of the Electrical Circuit
        Lab in which data can be collected.
        Students can analyze the collected data by creating graphs of the data and use the graphs in the conclusion
        tool.
        Harmonic Analyzer is also available for measuring and analyzing the data’s. </p><br>
      <h4>Engineering Workshop Laboratory</h4>
      <p align:"justify">Engineering workshop Lab covers all the basic engineering lab practices in the Civil,
        Mechanical, Electrical and Electronics areas.
        The lab deals with the various tools to be used and exercises to be practiced in the application of engineering.
        . </p>
